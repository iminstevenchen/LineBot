def query_mock_rag(keywords: str) -> list[str]:
    """
    根據關鍵字字串，從 MOCK 知識庫找出最相關的片段。
    正式版將替換為向量 DB 的 cosine similarity 搜尋。
    """
    results = []
    keyword_list = keywords.replace("、", " ").replace("，", " ").split()

    for chunk in KNOWLEDGE_CHUNKS:
        score = sum(1 for kw in chunk["keywords"] if any(kw in k or k in kw for k in keyword_list))
        if score > 0:
            results.append((score, chunk["text"]))

    # 依相關度排序，最多回傳 2 個片段
    results.sort(key=lambda x: x[0], reverse=True)
    return [text for _, text in results[:2]]
